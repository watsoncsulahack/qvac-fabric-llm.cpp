# --- KV cache size probing (measured, not theoretical) ---

declare -A KV_SIZE_MIB

probe_kv_sizes() {
    echo "--- Probing KV cache sizes (measured) ---"
    echo ""
    for pair in "${ALL_CONFIGS[@]}"; do
        k_type="${pair%%:*}"
        v_type="${pair##*:}"
        local output
        output=$("$BENCH" -v \
            "${SPLIT_ARGS[@]}" \
            -m "$MODEL_PATH" \
            --cache-type-k "$k_type" \
            --cache-type-v "$v_type" \
            -fa 1 -p 1 -n 0 -r 1 \
            2>&1) || {
            echo "  WARNING: could not probe KV size for K=$k_type V=$v_type" >&2
            continue
        }
        local total_mib
        total_mib=$(echo "$output" | grep "llama_kv_cache: size" | head -1 | sed 's/.*size = *//; s/ MiB.*//')
        if [ -n "$total_mib" ]; then
            KV_SIZE_MIB["$pair"]="$total_mib"
            echo "  K=$k_type V=$v_type: $total_mib MiB"
        else
            echo "  WARNING: could not parse KV size for K=$k_type V=$v_type" >&2
        fi
    done
    echo ""

    if [ ${#REF_CONFIGS[@]} -gt 0 ]; then
        echo "  --- ref impl  ---"
        for pair in "${REF_CONFIGS[@]}"; do
            k_type="${pair%%:*}"
            v_type="${pair##*:}"
            ref_key="ref-${k_type}:ref-${v_type}"
            local output
            output=$("$REF_BENCH" -v \
                "${SPLIT_ARGS[@]}" \
                -m "$MODEL_PATH" \
                --cache-type-k "$k_type" \
                --cache-type-v "$v_type" \
                -fa 1 -p 1 -n 0 -r 1 \
                2>&1) || {
                echo "  WARNING: could not probe KV size for ref K=$k_type V=$v_type" >&2
                continue
            }
            local total_mib
            total_mib=$(echo "$output" | grep "llama_kv_cache: size" | head -1 | sed 's/.*size = *//; s/ MiB.*//')
            if [ -n "$total_mib" ]; then
                KV_SIZE_MIB["$ref_key"]="$total_mib"
                echo "  K=ref-$k_type V=ref-$v_type: $total_mib MiB"
            else
                echo "  WARNING: could not parse KV size for ref K=$k_type V=$v_type" >&2
            fi
        done
        echo ""
    fi

    local f16_size="${KV_SIZE_MIB[f16:f16]:-}"
    if [ -n "$f16_size" ]; then
        echo "  f16 baseline: $f16_size MiB"
        for pair in "${ALL_CONFIGS[@]}"; do
            [ "$pair" = "f16:f16" ] && continue
            local sz="${KV_SIZE_MIB[$pair]:-}"
            if [ -n "$sz" ]; then
                local ratio
                ratio=$(echo "$f16_size $sz" | awk '{if ($2 > 0) printf "%.2f", $1/$2; else print "n/a"}')
                echo "  K=${pair%%:*} V=${pair##*:}: ${sz} MiB (${ratio}x compression)"
            fi
        done
        if [ ${#REF_CONFIGS[@]} -gt 0 ]; then
            for pair in "${REF_CONFIGS[@]}"; do
                ref_key="ref-${pair%%:*}:ref-${pair##*:}"
                local sz="${KV_SIZE_MIB[$ref_key]:-}"
                if [ -n "$sz" ]; then
                    local ratio
                    ratio=$(echo "$f16_size $sz" | awk '{if ($2 > 0) printf "%.2f", $1/$2; else print "n/a"}')
                    echo "  K=ref-${pair%%:*} V=ref-${pair##*:}: ${sz} MiB (${ratio}x compression)"
                fi
            done
        fi
    fi
    echo ""
}

probe_kv_sizes

get_compression() {
    local pair="$1"
    local f16_size="${KV_SIZE_MIB[f16:f16]:-}"
    local sz="${KV_SIZE_MIB[$pair]:-}"
    if [ -n "$f16_size" ] && [ -n "$sz" ]; then
        echo "$f16_size $sz" | awk '{if ($2 > 0) printf "%.2fx", $1/$2; else print "n/a"}'
    else
        echo "n/a"
    fi
}

# --- Extraction helpers (avg ± stdev) ---

extract_pp() {
    echo "$1" | grep -E '^\|' | grep -E '\bpp[0-9]' | awk -F'|' '{n=NF; gsub(/ /, "", $(n-1)); split($(n-1), a, "±"); print a[1]}' | tail -1
}

extract_pp_stdev() {
    echo "$1" | grep -E '^\|' | grep -E '\bpp[0-9]' | awk -F'|' '{n=NF; gsub(/ /, "", $(n-1)); split($(n-1), a, "±"); print a[2]}' | tail -1
}

extract_tg() {
    echo "$1" | grep -E '^\|' | grep -E '\btg[0-9]' | awk -F'|' '{n=NF; gsub(/ /, "", $(n-1)); split($(n-1), a, "±"); print a[1]}' | tail -1
}

extract_tg_stdev() {
    echo "$1" | grep -E '^\|' | grep -E '\btg[0-9]' | awk -F'|' '{n=NF; gsub(/ /, "", $(n-1)); split($(n-1), a, "±"); print a[2]}' | tail -1
}

run_bench() {
    local k_type="$1"
    local v_type="$2"
    local env_prefix="$3"
    local label="$4"
    local prompt_len="$5"
    local gen_len="$6"
    local reps="$7"
    local bench_bin="${8:-$BENCH}"

    local output
    output=$(env $env_prefix "$bench_bin" \
        "${SPLIT_ARGS[@]}" \
        -m "$MODEL_PATH" \
        --cache-type-k "$k_type" \
        --cache-type-v "$v_type" \
        -fa 1 \
        -p "$prompt_len" \
        -n "$gen_len" \
        -r "$reps" \
        2>&1) || {
        echo "  FAILED: $label (K=$k_type V=$v_type)" >&2
        echo "$output" | tail -5 >&2
        return 1
    }

    if echo "$output" | grep -q "error:"; then
        echo "  FAILED: $label (K=$k_type V=$v_type) — $(echo "$output" | grep 'error:' | head -1)" >&2
        return 1
    fi

    local pp pp_sd tg tg_sd
    pp=$(extract_pp "$output")
    pp_sd=$(extract_pp_stdev "$output")
    tg=$(extract_tg "$output")
    tg_sd=$(extract_tg_stdev "$output")

    if [ -z "$pp" ] || [ -z "$tg" ]; then
        echo "  FAILED: $label — could not parse t/s from output" >&2
        return 1
    fi

    pp_sd="${pp_sd:-0.00}"
    tg_sd="${tg_sd:-0.00}"

    echo "$pp $pp_sd $tg $tg_sd"
}

