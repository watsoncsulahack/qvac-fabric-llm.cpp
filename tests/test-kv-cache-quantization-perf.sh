# Benchmark KV cache quantization throughput (prefill pp & decode tg t/s).
# Auto-detects coopmat1/coopmat2 GPU support and compares cooperative matrix
# flash attention vs scalar fallback for TBQ/PQ quantizations.
#
# Usage:
#   tests/test-kv-cache-quantization-perf.sh [options] [build_dir]
#
# Options:
#   -c <tiny|small|mid|large>     Size config preset (repeatable, default: small)
#   -r <n>                       Override preset repetitions (applies to all configs)
#   -m <preset>                  Model preset (see -h for list, default: mistral-q4ks)
#   --all                        Run all model presets sequentially
#   --csv <file>                 Write results to specific CSV file (auto-generated by default)
#   --ref-impl <path>            Reference turboquant repo (default: ../llama-cpp-turboquant, e.g. TheTom)
#   --ref-ks <type> [<type>...]  K cache types from ref impl (default: turbo3 turbo4)
#   --ref-vs <type> [<type>...]  V cache types from ref impl (default: turbo3 turbo4)
#
# Config presets (pp / tg / reps):
#   tiny   — pp 128,  tg 32,  reps 1  (very quick smoke test)
#   small  — pp 128,  tg 32,  reps 3  (quick with stdev)
#   mid    — pp 512,  tg 128, reps 3  (balanced with stdev)
#   large  — pp 2048, tg 512, reps 5  (full stress test with stdev)
#
# Without -c, env vars PROMPT_LEN, GEN_LEN, REPS are used directly.
#
# Environment variables:
#   MODEL_DIR    — directory for model files (default: models)
#   MODEL_NAME   — GGUF filename
#   MODEL_URL    — download URL
#   PROMPT_LEN   — prompt length when no -c given (default: 512)
#   GEN_LEN      — gen length when no -c given (default: 128)
#   REPS         — repetitions when no -c given (default: 3)
#   CSV          — set to 0 to disable auto CSV output (default: 1)
#   SKIP_COOPMAT_COMPARE — set to 1 to skip the coopmat-disabled runs
#   FORCE_COOPMAT — override autodetect: "cm1", "cm2", "both", or "none"
#
# Examples:
#   tests/test-kv-cache-quantization-perf.sh -c small -c mid --csv results.csv
#   tests/test-kv-cache-quantization-perf.sh -c large -r 10 --csv results.csv build
