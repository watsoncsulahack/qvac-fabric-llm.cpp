# --- Generate randomized offset slices for each n_ctx ---
# Each n_ctx in the sweep gets its own file starting at a random byte-aligned
# offset into the dataset. This ensures measurements at different context sizes
# are statistically independent (they evaluate different text).

generate_offset_files() {
    local dataset="$1"
    shift
    local ctx_list=("$@")
    local file_size
    file_size=$(stat -c%s "$dataset" 2>/dev/null || stat -f%z "$dataset" 2>/dev/null || echo 0)

    local min_reserve=50000
    local max_offset=$(( file_size - min_reserve ))
    if [ "$max_offset" -lt 0 ]; then
        max_offset=0
    fi

    local offsets=()
    if [ "$max_offset" -gt 0 ]; then
        # Seed $RANDOM so offsets are reproducible across reruns even when the
        # cached slice files are missing (first run, --regen-slices, or another
        # machine). Override by exporting SLICE_SEED before invoking the script
        # when you actually want a fresh random draw.
        RANDOM=${SLICE_SEED:-42}
        for ctx in "${ctx_list[@]}"; do
            offsets+=("$(( RANDOM * 32768 + RANDOM ))")
        done
        # sort and deduplicate to spread them out, then map to range
        local i=0
        for ctx in "${ctx_list[@]}"; do
            local slice_file="${DATASET_DIR}/wiki.test.offset_${ctx}.raw"

            # Reuse existing slice for reproducibility across reruns. The random
            # offset selection is the only non-deterministic step in the script,
            # so keeping the same slices makes PPL numbers directly comparable
            # run-to-run. Delete the offset files (or pass --regen-slices) to
            # force a fresh randomized slice.
            if [ "${REGEN_SLICES:-0}" != "1" ] && [ -s "$slice_file" ]; then
                local slice_size
                slice_size=$(stat -c%s "$slice_file" 2>/dev/null || stat -f%z "$slice_file" 2>/dev/null || echo 0)
                # Recover the original byte offset by subtracting the slice
                # size from the dataset size (slices are always tail suffixes).
                local off_recovered=$(( file_size - slice_size ))
                echo "  n_ctx=$ctx: reusing offset=$off_recovered (${slice_size} bytes) ← $slice_file" >&2
                OFFSET_FILES[$ctx]="$slice_file"
                i=$((i + 1))
                continue
            fi

            local raw_off="${offsets[$i]}"
            local off=$(( raw_off % max_offset ))
            # Align to a newline boundary so we don't start mid-word
            local aligned_off
            aligned_off=$(tail -c +"$((off + 1))" "$dataset" | grep -bom1 $'\n' | head -1 | cut -d: -f1 || echo 0)
            off=$(( off + aligned_off + 1 ))
            if [ "$off" -ge "$file_size" ]; then
                off=0
            fi
            tail -c +"$((off + 1))" "$dataset" > "$slice_file"
            local slice_size
            slice_size=$(stat -c%s "$slice_file" 2>/dev/null || stat -f%z "$slice_file" 2>/dev/null || echo 0)
            echo "  n_ctx=$ctx: offset=$off (${slice_size} bytes) → $slice_file" >&2
            OFFSET_FILES[$ctx]="$slice_file"
            i=$((i + 1))
        done
    else
        for ctx in "${ctx_list[@]}"; do
            OFFSET_FILES[$ctx]="$dataset"
        done
    fi
}

