    cp "$CSV_FILE" "$CSV_FINAL"
    rm -f "$CSV_FILE"
    echo ""
    echo "=========================================="
    echo " Output files"
    echo "=========================================="
    echo " CSV: $(readlink -f "$CSV_FINAL") (${#CSV_ROWS[@]} rows)"
    echo " Log: $(readlink -f "$LOG_FILE")"
    echo "=========================================="
fi
