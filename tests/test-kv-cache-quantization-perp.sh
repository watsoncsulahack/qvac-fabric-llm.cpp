# --- Helper: run perplexity once ---

run_perplexity_once() {
    local k_type="$1" v_type="$2" n_ctx="$3" n_chunks="$4"
    local bin="${5:-$PERPLEXITY}"

    local env_prefix=""
    if [ "${GGML_TQ_NORM_CORRECTION:-0}" = "1" ]; then
        env_prefix="GGML_TQ_NORM_CORRECTION=1 "
    fi

    local chunks_args=""
    if [ "$n_chunks" -gt 0 ]; then
        chunks_args="--chunks $n_chunks"
    fi

    # Use offset file for this n_ctx if available
    local data_file="${OFFSET_FILES[$n_ctx]:-$DATASET_FILE}"

    # Force single-GPU execution so PPL is reproducible across hosts. Without
    # --split-mode none, llama-perplexity defaults to splitting decoder layers
    # across all visible devices, which introduces cross-device numerical
    # differences that can drift the baseline by more than the QJL/FA signal
    # we're looking for. Matches the default in test-kv-cache-quantization-perf.sh.
    echo "  \$ ${env_prefix}${bin} \\" >&2
    echo "      -m $MODEL_PATH \\" >&2
    echo "      -f $data_file \\" >&2
    echo "      --cache-type-k $k_type --cache-type-v $v_type \\" >&2
    echo "      --split-mode none \\" >&2
    echo "      -fa $FA_FLAG -n $n_ctx ${chunks_args}" >&2

    local start_time end_time elapsed_ms elapsed_s output ppl

    start_time=$(date +%s%N)

    # shellcheck disable=SC2086
    output=$("$bin" \
        -m "$MODEL_PATH" \
        -f "$data_file" \
        --cache-type-k "$k_type" \
        --cache-type-v "$v_type" \
        --split-mode none \
        -fa "$FA_FLAG" \
        -n "$n_ctx" \
        $chunks_args \
        2>&1) || {
        echo "FAILED" >&2
        echo "$output" | tail -10 >&2
        return 1
    }

    end_time=$(date +%s%N)
    elapsed_ms=$(( (end_time - start_time) / 1000000 ))
    elapsed_s=$(echo "$elapsed_ms" | awk '{printf "%.2f", $1/1000}')

    local ppl_sd_pair
    ppl_sd_pair=$(extract_ppl_and_sd "$output")
    ppl=$(echo "$ppl_sd_pair" | awk '{print $1}')
    local ppl_sd
    ppl_sd=$(echo "$ppl_sd_pair" | awk '{print $2}')
    if [ -z "$ppl" ]; then
        echo "PARSE_FAILED" >&2
        echo "$output" | tail -10 >&2
        return 1
    fi

    echo "$ppl $ppl_sd $elapsed_s"
}

# --- Helper: run across n_ctx values, compute mean and stdev ---

run_perplexity_sweep() {
    local k_type="$1" v_type="$2" n_chunks="$3"
    shift 3
    local ctx_list=("$@")
    local n_runs=${#ctx_list[@]}

    local ppls=() ppl_sds=() times=()

    for (( i=0; i<n_runs; i++ )); do
        local ctx="${ctx_list[$i]}"
        local result
        result=$(run_perplexity_once "$k_type" "$v_type" "$ctx" "$n_chunks" "${SWEEP_BIN:-}") || return 1
        local ppl_val ppl_sd_val time_val
        ppl_val=$(echo "$result" | awk '{print $1}')
        ppl_sd_val=$(echo "$result" | awk '{print $2}')
        time_val=$(echo "$result" | awk '{print $3}')
        ppls+=("$ppl_val")
        ppl_sds+=("$ppl_sd_val")
        times+=("$time_val")
        if [ "$n_runs" -gt 1 ]; then
            echo "    n_ctx=$ctx: PPL=${ppl_val}±${ppl_sd_val} (${time_val}s)" >&2
        fi
    done

    local ppl_mean ppl_sweep_sd ppl_chunk_sd time_mean time_stdev
    if [ "$n_runs" -eq 1 ]; then
        ppl_mean="${ppls[0]}"
        ppl_sweep_sd="0.0000"
        ppl_chunk_sd="${ppl_sds[0]}"
        time_mean="${times[0]}"
        time_stdev="0.00"
    else
        ppl_mean=$(printf '%s\n' "${ppls[@]}" | awk '{s+=$1; n++} END {printf "%.4f", s/n}')
        ppl_sweep_sd=$(printf '%s\n' "${ppls[@]}" | awk -v mean="$ppl_mean" '{d=$1-mean; ss+=d*d; n++} END {printf "%.4f", sqrt(ss/(n-1))}')
        ppl_chunk_sd=$(printf '%s\n' "${ppl_sds[@]}" | awk '{s+=$1*$1; n++} END {printf "%.4f", sqrt(s/n)}')
        time_mean=$(printf '%s\n' "${times[@]}" | awk '{s+=$1; n++} END {printf "%.2f", s/n}')
        time_stdev=$(printf '%s\n' "${times[@]}" | awk -v mean="$time_mean" '{d=$1-mean; ss+=d*d; n++} END {printf "%.2f", sqrt(ss/(n-1))}')
    fi

    # Output: ppl_mean sweep_sd chunk_sd time_mean time_stdev
    echo "$ppl_mean $ppl_sweep_sd $ppl_chunk_sd $time_mean $time_stdev"
}

