#!/usr/bin/env bash
#
# Test KV cache quantization quality by comparing perplexity across cache types.
# Downloads a small model and wikitext dataset if not already present.
#
# Usage:
#   tests/test-kv-cache-quantization-perp.sh [options] [build_dir]
#
# Options:
#   -c <tiny|tiny2|small|mid|large>  Size config preset (repeatable, default: small)
#   -m <preset>                      Model preset (see -h for list, default: mistral-q4ks)
#   --all                            Run all model presets sequentially
#   --mixed-only                     Skip same-type tests, only run mixed K/V
#   --no-fa                          Disable flash attention (-fa off), uses scalar MUL_MAT fallback
#   --regen-slices                   Regenerate randomized dataset slices even if they already exist
#                                    (default: reuse existing wiki.test.offset_<n_ctx>.raw files so
#                                    reruns are byte-for-byte reproducible)
#   --csv <file>                     Write results to specific CSV file (auto-generated by default)
#   --ks <type> [<type>...]          K cache types to sweep (overrides built-in ALL_CONFIGS)
#   --vs <type> [<type>...]          V cache types to sweep (overrides built-in ALL_CONFIGS)
#                                    When --ks or --vs is given, the Cartesian product is run.
#                                    In --no-fa mode, omitted V types default to f16 because
#                                    quantized V cache requires flash attention.
#   --ref-impl <path>                Reference turboquant repo (default: ../llama-cpp-turboquant, e.g. TheTom)
#   --ref-ks <type> [<type>...]      K cache types from ref impl (default: turbo3 turbo4)
#   --ref-vs <type> [<type>...]      V cache types from ref impl (default: turbo3 turbo4)
#
# Config presets (n_chunks / n_ctx sweep):
#   tiny   — 1 chunk,  n_ctx 64               (single quick check, no stdev)
#   tiny2  — 1 chunk,  n_ctx 64,72            (smoke test with stdev)
#   small  — 2 chunks, n_ctx 64,128           (quick, 2 context sizes)
#   mid    — 4 chunks, n_ctx 448,512          (balanced, near standard 512)
#   large  — 8 chunks, n_ctx 448,512,1024,2048  (thorough, 4 context sizes)
#   huge   — 16 chunks, n_ctx 448,512,1024,2048,4096  (full sweep, 5 context sizes)
#
# Each n_ctx value in the sweep gets its own randomized slice of the dataset,
# so measurements at different context sizes are statistically independent.
# Set DATASET_DIR=wikitext-103-raw for a larger dataset (~100x more text).
#
# Slices are cached on disk as wiki.test.offset_<n_ctx>.raw and reused across
# reruns so PPL numbers are directly comparable when iterating on a fix. Pass
# --regen-slices (or delete the offset files) to force regeneration. Offset
# generation itself uses a fixed $RANDOM seed (SLICE_SEED, default 42), so even
# a fresh regeneration is byte-for-byte reproducible. Export SLICE_SEED=<n>
# to draw a different but still deterministic set of offsets.
#
# WARNING: Presets smaller than 'mid' (tiny, tiny2, small) are prone to statistical noise.
# At very small context sizes, quantization error can occasionally result in lower PPL
# than f16 due to numerical artifacts or the "noise" acting as accidental regularization.
# Use 'mid' or 'large' for reliable quality comparisons.
#
# Perplexity is deterministic — repeating the same run gives identical PPL.
# Instead, presets sweep multiple n_ctx values to measure quality stability
# across context lengths. Mean/stdev are computed across the n_ctx sweep.
#
# Environment variables:
#   MODEL_DIR    — directory for model files (default: models)
#   MODEL_NAME   — GGUF filename
#   MODEL_URL    — download URL
#   CSV          — set to 0 to disable auto CSV output (default: 1)
#   MAX_PPL_REGRESSION_PCT — max allowed PPL regression vs f16 (default: 21)
#
# Examples:
#   tests/test-kv-cache-quantization-perp.sh -c small -c mid --csv ppl.csv
#   tests/test-kv-cache-quantization-perp.sh -c large --csv ppl.csv build
#
