        exceeded=$(echo "$ppl $baseline_ppl $MAX_PPL_REGRESSION_PCT" | \
            awk '{regression = (($1 - $2) / $2) * 100; print (regression > $3) ? "1" : "0"}')

        if [ "$exceeded" = "1" ]; then
            k_type="${pair%%:*}"
            v_type="${pair##*:}"
            regression=$(echo "$ppl $baseline_ppl" | awk '{printf "%.2f", (($1 - $2) / $2) * 100}')
            echo "FAILED: K=$k_type V=$v_type PPL regression ${regression}% exceeds ${MAX_PPL_REGRESSION_PCT}% threshold"
            num_failed=$((num_failed + 1))
        fi
    done

    if [ "$MIXED_ONLY" = false ]; then
    pq3_ppl="${ppl_results[pq3_0:pq3_0]}"
    pq4_ppl="${ppl_results[pq4_0:pq4_0]}"
    pq4_worse=$(echo "$pq4_ppl $pq3_ppl" | awk '{print ($1 > $2) ? "1" : "0"}')
    if [ "$pq4_worse" = "1" ]; then
        echo "FAILED: pq4_0 PPL ($pq4_ppl) should be <= pq3_0 PPL ($pq3_ppl)"
        num_failed=$((num_failed + 1))
    fi

    tbq4pq4_ppl="${ppl_results[tbq4_0:pq4_0]}"
    tbq4pq4_worse=$(echo "$tbq4pq4_ppl $pq4_ppl" | awk '{print ($1 > $2) ? "1" : "0"}')
    if [ "$tbq4pq4_worse" = "1" ]; then
        echo "WARNING: tbq4_0:pq4_0 PPL ($tbq4pq4_ppl) should be <= pq4_0:pq4_0 PPL ($pq4_ppl)"
    fi
    tbq3pq3_ppl="${ppl_results[tbq3_0:pq3_0]}"
    tbq3pq3_worse=$(echo "$tbq3pq3_ppl $pq3_ppl" | awk '{print ($1 > $2) ? "1" : "0"}')
    if [ "$tbq3pq3_worse" = "1" ]; then
        echo "WARNING: tbq3_0:pq3_0 PPL ($tbq3pq3_ppl) should be <= pq3_0:pq3_0 PPL ($pq3_ppl)"
    fi
    fi

    # --- Write CSV rows ---

    CTX_SWEEP_STR="${N_CTX_LIST[*]}"

    if [ -n "$CSV_FILE" ]; then
        csv_regression_sd() {
            echo "$1 $2" | awk '{printf "%.2f", ($1 / $2) * 100}'
        }
        csv_ratio_sd() {
            echo "$1 $2 $3 $4" | awk '{
                if ($3 > 0) {
                    a = ($2/$3)^2; b = ($1*$4/($3*$3))^2;
                    printf "%.2f", sqrt(a+b)
                } else print ""
            }'
        }

        NC_FLAG="off"
        for pair in "${RUN_CONFIGS[@]}"; do
            k_type="${pair%%:*}"
            v_type="${pair##*:}"
            ppl="${ppl_results[$pair]}"
            sweep_sd="${ppl_sweep_sd_results[$pair]}"
            chunk_sd="${ppl_chunk_sd_results[$pair]}"
            elapsed="${time_results[$pair]}"
            elapsed_sd="${time_sd_results[$pair]}"
            is_m="no"
            [ "$k_type" != "$v_type" ] && is_m="yes"
            if [ "$pair" = "f16:f16" ]; then
                ppl_vs_f16="0.00"
                ppl_vs_f16_sd="0.00"
                time_vs_f16="1.00"
                time_vs_f16_sd="0.00"
            else
                ppl_vs_f16=$(echo "$ppl $baseline_ppl" | awk '{printf "%.2f", (($1 - $2) / $2) * 100}')
                ppl_vs_f16_sd=$(csv_regression_sd "$sweep_sd" "$baseline_ppl")
                time_vs_f16=$(echo "$elapsed $baseline_time" | awk '{if ($2 > 0) printf "%.2f", $1/$2; else print ""}')
                time_vs_f16_sd=$(csv_ratio_sd "$elapsed" "$elapsed_sd" "$baseline_time" "$baseline_time_sd")
            fi
            csv_append "$k_type" "$v_type" "$is_m" "$N_CHUNKS_CFG" "$CTX_SWEEP_STR" "$ppl" "$sweep_sd" "$chunk_sd" "$elapsed" "$elapsed_sd" "$ppl_vs_f16" "$ppl_vs_f16_sd" "$time_vs_f16" "$time_vs_f16_sd" "$cfg"
        done

        NC_FLAG="on"
        for pair in "${RUN_CONFIGS[@]}"; do
            [ -z "${ppl_results_a[$pair]:-}" ] && continue
            k_type="${pair%%:*}"
            v_type="${pair##*:}"
            ppl="${ppl_results_a[$pair]}"
            sweep_sd="${ppl_sweep_sd_results_a[$pair]}"
            chunk_sd="${ppl_chunk_sd_results_a[$pair]}"
            elapsed="${time_results_a[$pair]}"
            elapsed_sd="${time_sd_results_a[$pair]}"
            is_m="no"
            [ "$k_type" != "$v_type" ] && is_m="yes"
            ppl_vs_f16=$(echo "$ppl $baseline_ppl" | awk '{printf "%.2f", (($1 - $2) / $2) * 100}')
            ppl_vs_f16_sd=$(csv_regression_sd "$sweep_sd" "$baseline_ppl")
            time_vs_f16=$(echo "$elapsed $baseline_time" | awk '{if ($2 > 0) printf "%.2f", $1/$2; else print ""}')
            time_vs_f16_sd=$(csv_ratio_sd "$elapsed" "$elapsed_sd" "$baseline_time" "$baseline_time_sd")
            csv_append "$k_type" "$v_type" "$is_m" "$N_CHUNKS_CFG" "$CTX_SWEEP_STR" "$ppl" "$sweep_sd" "$chunk_sd" "$elapsed" "$elapsed_sd" "$ppl_vs_f16" "$ppl_vs_f16_sd" "$time_vs_f16" "$time_vs_f16_sd" "$cfg"
        done
        NC_FLAG="off"

        if [ ${#REF_CONFIGS[@]} -gt 0 ]; then
            NC_FLAG="off"
            for pair in "${REF_CONFIGS[@]}"; do
                ref_key="ref-${pair%%:*}:ref-${pair##*:}"
                [ -z "${ppl_results[$ref_key]:-}" ] && continue
                k_disp="ref-${pair%%:*}"
                v_disp="ref-${pair##*:}"
                ppl="${ppl_results[$ref_key]}"
                sweep_sd="${ppl_sweep_sd_results[$ref_key]}"
                chunk_sd="${ppl_chunk_sd_results[$ref_key]}"
                elapsed="${time_results[$ref_key]}"
                elapsed_sd="${time_sd_results[$ref_key]}"
                is_m="no"
                [ "${pair%%:*}" != "${pair##*:}" ] && is_m="yes"
                ppl_vs_f16=$(echo "$ppl $baseline_ppl" | awk '{printf "%.2f", (($1 - $2) / $2) * 100}')
                ppl_vs_f16_sd=$(csv_regression_sd "$sweep_sd" "$baseline_ppl")
                time_vs_f16=$(echo "$elapsed $baseline_time" | awk '{if ($2 > 0) printf "%.2f", $1/$2; else print ""}')
                time_vs_f16_sd=$(csv_ratio_sd "$elapsed" "$elapsed_sd" "$baseline_time" "$baseline_time_sd")
                csv_append "$k_disp" "$v_disp" "$is_m" "$N_CHUNKS_CFG" "$CTX_SWEEP_STR" "$ppl" "$sweep_sd" "$chunk_sd" "$elapsed" "$elapsed_sd" "$ppl_vs_f16" "$ppl_vs_f16_sd" "$time_vs_f16" "$time_vs_f16_sd" "$cfg"
            done
        fi
    fi

    # Clean up per-config associative arrays
    unset ppl_results ppl_sweep_sd_results ppl_chunk_sd_results time_results time_sd_results
    unset ppl_results_a ppl_sweep_sd_results_a ppl_chunk_sd_results_a time_results_a time_sd_results_a

done  # end config loop

echo ""
echo "GPU device: $GPU_DEVICE"
