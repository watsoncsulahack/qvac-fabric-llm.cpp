# TBQ/PQ types that are affected by norm correction
is_tq_type() {
    case "$1" in
        tbq3_0|tbq4_0|pq3_0|pq4_0) return 0 ;;
        *) return 1 ;;
    esac
}

is_mixed() {
    local k="$1" v="$2"
    [ "$k" != "$v" ]
}

for cfg in "${CONFIGS[@]}"; do
    N_CHUNKS_CFG=$(config_n_chunks "$cfg")
    read -r -a N_CTX_LIST <<< "$(config_n_ctx_list "$cfg")"
    N_RUNS=${#N_CTX_LIST[@]}
    HAS_STDEV=1

    echo ""
    echo "############################################"
    echo " Config: $cfg  (chunks=$N_CHUNKS_CFG, n_ctx=[${N_CTX_LIST[*]}])"
    echo "############################################"
    echo ""

    declare -A ppl_results ppl_sweep_sd_results ppl_chunk_sd_results time_results time_sd_results
    declare -A ppl_results_a ppl_sweep_sd_results_a ppl_chunk_sd_results_a time_results_a time_sd_results_a

    # --- Helper: format value±sd (only shows ± when HAS_STDEV) ---
    fmt_pm() { [ "$HAS_STDEV" -eq 1 ] && echo "${1}±${2}" || echo "$1"; }

    fmt_regression() {
        local ppl="$1" ppl_sd="$2" base="$3"
        local reg reg_sd
        reg=$(echo "$ppl $base" | awk '{printf "%.2f", (($1 - $2) / $2) * 100}')
        if [ "$HAS_STDEV" -eq 1 ]; then
            reg_sd=$(echo "$ppl_sd $base" | awk '{printf "%.2f", ($1 / $2) * 100}')
            printf "%+.2f±%s%%" "$reg" "$reg_sd"
        else
            printf "%+.2f%%" "$reg"
        fi
    }

    fmt_slowdown() {
        local t="$1" t_sd="$2" base_t="$3" base_t_sd="$4"
        local ratio ratio_sd
        ratio=$(echo "$t $base_t" | awk '{if ($2 > 0) printf "%.1f", $1/$2; else {print "n/a"; exit}}')
        if [ "$HAS_STDEV" -eq 1 ]; then
            ratio_sd=$(echo "$t $t_sd $base_t $base_t_sd" | awk '{
                if ($3 > 0) {
                    a = ($2/$3)^2; b = ($1*$4/($3*$3))^2;
                    printf "%.1f", sqrt(a+b)
                } else print "0.0"
            }')
            echo "${ratio}±${ratio_sd}x"
        else
            echo "${ratio}x"
        fi
    }

    # --- Determine which configs to run ---

    RUN_CONFIGS=()
    for pair in "${ALL_CONFIGS[@]}"; do
        k_type="${pair%%:*}"
        v_type="${pair##*:}"
        if [ "$MIXED_ONLY" = true ] && [ "$k_type" = "$v_type" ] && [ "$k_type" != "f16" ]; then
            continue
        fi
        RUN_CONFIGS+=("$pair")
    done

    # --- Count total jobs for ETA ---

    total_jobs=0
    for pair in "${RUN_CONFIGS[@]}"; do
        total_jobs=$((total_jobs + 1))
        k_type="${pair%%:*}"; v_type="${pair##*:}"
        if is_tq_type "$k_type" || is_tq_type "$v_type"; then
            total_jobs=$((total_jobs + 1))
        fi
    done
    if [ ${#REF_CONFIGS[@]} -gt 0 ]; then
        total_jobs=$((total_jobs + ${#REF_CONFIGS[@]}))
    fi
    completed_jobs=0
    bench_start_time=$(date +%s)

    fmt_eta() {
        local done="$1" total="$2" start="$3"
        if [ "$done" -eq 0 ]; then echo "estimating..."; return; fi
        local now elapsed avg remaining
        now=$(date +%s)
        elapsed=$((now - start))
        avg=$((elapsed / done))
        remaining=$(( avg * (total - done) ))
        if [ "$remaining" -ge 3600 ]; then
            printf "%dh%02dm" $((remaining/3600)) $(( (remaining%3600)/60 ))
        elif [ "$remaining" -ge 60 ]; then
            printf "%dm%02ds" $((remaining/60)) $((remaining%60))
        else
            printf "%ds" "$remaining"
        fi
    }

    # --- Run all configs ---

    export GGML_TQ_NORM_CORRECTION=0
    for pair in "${RUN_CONFIGS[@]}"; do
        k_type="${pair%%:*}"
        v_type="${pair##*:}"

        bpw_k=$(get_bpw "$k_type")
        bpw_v=$(get_bpw "$v_type")
        bpw_avg=$(echo "$bpw_k $bpw_v" | awk '{printf "%.2f", ($1 + $2) / 2}')

        eta=$(fmt_eta "$completed_jobs" "$total_jobs" "$bench_start_time")
        echo "--- [$((completed_jobs+1))/$total_jobs, ETA $eta] Running perplexity with K=$k_type V=$v_type [$cfg] (BPW=$bpw_avg) ---"

        result=$(run_perplexity_sweep "$k_type" "$v_type" "$N_CHUNKS_CFG" "${N_CTX_LIST[@]}") || {
            echo "FAILED: perplexity run crashed for K=$k_type V=$v_type"
            exit 1
        }

        ppl_results["$pair"]=$(echo "$result" | awk '{print $1}')
        ppl_sweep_sd_results["$pair"]=$(echo "$result" | awk '{print $2}')
        ppl_chunk_sd_results["$pair"]=$(echo "$result" | awk '{print $3}')
        time_results["$pair"]=$(echo "$result" | awk '{print $4}')
        time_sd_results["$pair"]=$(echo "$result" | awk '{print $5}')
        completed_jobs=$((completed_jobs + 1))
        echo "  K=$k_type V=$v_type PPL = ${ppl_results[$pair]} (sweep±${ppl_sweep_sd_results[$pair]}, chunk±${ppl_chunk_sd_results[$pair]})  ($(fmt_pm "${time_results[$pair]}" "${time_sd_results[$pair]}")s)"
        echo ""

        if is_tq_type "$k_type" || is_tq_type "$v_type"; then
            eta=$(fmt_eta "$completed_jobs" "$total_jobs" "$bench_start_time")
            echo "--- [$((completed_jobs+1))/$total_jobs, ETA $eta] Running perplexity with K=$k_type V=$v_type [$cfg] (α, BPW=$bpw_avg) ---"
            export GGML_TQ_NORM_CORRECTION=1
            result=$(run_perplexity_sweep "$k_type" "$v_type" "$N_CHUNKS_CFG" "${N_CTX_LIST[@]}") || {
                echo "FAILED: perplexity run crashed for K=$k_type V=$v_type (α)"
                exit 1
            }
            export GGML_TQ_NORM_CORRECTION=0
            ppl_results_a["$pair"]=$(echo "$result" | awk '{print $1}')
            ppl_sweep_sd_results_a["$pair"]=$(echo "$result" | awk '{print $2}')
            ppl_chunk_sd_results_a["$pair"]=$(echo "$result" | awk '{print $3}')
            time_results_a["$pair"]=$(echo "$result" | awk '{print $4}')
            time_sd_results_a["$pair"]=$(echo "$result" | awk '{print $5}')
            completed_jobs=$((completed_jobs + 1))
            echo "  K=$k_type V=$v_type (α) PPL = ${ppl_results_a[$pair]} (sweep±${ppl_sweep_sd_results_a[$pair]}, chunk±${ppl_chunk_sd_results_a[$pair]})  ($(fmt_pm "${time_results_a[$pair]}" "${time_sd_results_a[$pair]}")s)"
            echo ""
        fi
    done

    # --- Run reference implementation configs ---

    if [ ${#REF_CONFIGS[@]} -gt 0 ]; then
        export GGML_TQ_NORM_CORRECTION=0
        SWEEP_BIN="$REF_PERPLEXITY"
        for pair in "${REF_CONFIGS[@]}"; do
            k_type="${pair%%:*}"
            v_type="${pair##*:}"
            ref_key="ref-${k_type}:ref-${v_type}"

            eta=$(fmt_eta "$completed_jobs" "$total_jobs" "$bench_start_time")
            echo "--- [$((completed_jobs+1))/$total_jobs, ETA $eta] Running perplexity with K=ref-$k_type V=ref-$v_type [$cfg] (ref impl) ---"

            result=$(run_perplexity_sweep "$k_type" "$v_type" "$N_CHUNKS_CFG" "${N_CTX_LIST[@]}") || {
                echo "FAILED: ref perplexity run crashed for K=$k_type V=$v_type"
                continue
            }

            ppl_results["$ref_key"]=$(echo "$result" | awk '{print $1}')
            ppl_sweep_sd_results["$ref_key"]=$(echo "$result" | awk '{print $2}')
            ppl_chunk_sd_results["$ref_key"]=$(echo "$result" | awk '{print $3}')
            time_results["$ref_key"]=$(echo "$result" | awk '{print $4}')
            time_sd_results["$ref_key"]=$(echo "$result" | awk '{print $5}')
            completed_jobs=$((completed_jobs + 1))
            echo "  K=ref-$k_type V=ref-$v_type PPL = ${ppl_results[$ref_key]} (sweep±${ppl_sweep_sd_results[$ref_key]}, chunk±${ppl_chunk_sd_results[$ref_key]})  ($(fmt_pm "${time_results[$ref_key]}" "${time_sd_results[$ref_key]}")s)"
            echo ""
        done
        unset SWEEP_BIN
    fi

    # --- Print unified summary ---

    baseline_ppl="${ppl_results[f16:f16]}"
    # baseline_ppl_sd="${ppl_sd_results[f16:f16]}"
    baseline_time="${time_results[f16:f16]}"
    baseline_time_sd="${time_sd_results[f16:f16]}"

    echo ""
    echo "=========================================="
    echo " Results Summary [$cfg]"
    echo "=========================================="
    printf "  %-10s %-10s %-6s %10s %10s %10s %14s %10s\n" \
        "K type" "V type" "BPW" "PPL" "sweep±" "chunk±" "vs f16" "Time"
    printf "  %-10s %-10s %-6s %10s %10s %10s %14s %10s\n" \
        "------" "------" "---" "---" "-------" "------" "------" "----"

    for pair in "${RUN_CONFIGS[@]}"; do
        k_type="${pair%%:*}"
        v_type="${pair##*:}"
        ppl="${ppl_results[$pair]}"
        sweep_sd="${ppl_sweep_sd_results[$pair]}"
        chunk_sd="${ppl_chunk_sd_results[$pair]}"
        elapsed="${time_results[$pair]}"
        elapsed_sd="${time_sd_results[$pair]}"

        bpw_k=$(get_bpw "$k_type")
        bpw_v=$(get_bpw "$v_type")
        bpw_avg=$(echo "$bpw_k $bpw_v" | awk '{printf "%.2f", ($1 + $2) / 2}')

        time_disp="$(fmt_pm "$elapsed" "$elapsed_sd")s"

        if [ "$pair" = "f16:f16" ]; then
            printf "  %-10s %-10s %-6s %10s %10s %10s %14s %10s\n" \
                "$k_type" "$v_type" "$bpw_avg" "$ppl" "$sweep_sd" "$chunk_sd" "(baseline)" "$time_disp"
        else
            reg_disp=$(fmt_regression "$ppl" "$sweep_sd" "$baseline_ppl")
            printf "  %-10s %-10s %-6s %10s %10s %10s %14s %10s\n" \
                "$k_type" "$v_type" "$bpw_avg" "$ppl" "$sweep_sd" "$chunk_sd" "$reg_disp" "$time_disp"
        fi

        if [ -n "${ppl_results_a[$pair]:-}" ]; then
            ppl_a="${ppl_results_a[$pair]}"
            sweep_sd_a="${ppl_sweep_sd_results_a[$pair]}"
            chunk_sd_a="${ppl_chunk_sd_results_a[$pair]}"
            elapsed_a="${time_results_a[$pair]}"
            elapsed_sd_a="${time_sd_results_a[$pair]}"
            time_disp_a="$(fmt_pm "$elapsed_a" "$elapsed_sd_a")s"
            reg_disp_a=$(fmt_regression "$ppl_a" "$sweep_sd_a" "$baseline_ppl")
            printf "  %-10s %-10s %-6s %10s %10s %10s %14s %10s\n" \
                "  └ α" "" "-" "$ppl_a" "$sweep_sd_a" "$chunk_sd_a" "$reg_disp_a" "$time_disp_a"
        fi
    done

    if [ ${#REF_CONFIGS[@]} -gt 0 ]; then
        printf "  %-10s %-10s %-6s %10s %10s %10s %14s %10s\n" \
            "---ref---" "---ref---" "---" "---" "-------" "------" "------" "----"
        for pair in "${REF_CONFIGS[@]}"; do
            ref_key="ref-${pair%%:*}:ref-${pair##*:}"
            [ -z "${ppl_results[$ref_key]:-}" ] && continue
            k_disp="ref-${pair%%:*}"
            v_disp="ref-${pair##*:}"
            ppl="${ppl_results[$ref_key]}"
            sweep_sd="${ppl_sweep_sd_results[$ref_key]}"
            chunk_sd="${ppl_chunk_sd_results[$ref_key]}"
            elapsed="${time_results[$ref_key]}"
            elapsed_sd="${time_sd_results[$ref_key]}"

            time_disp="$(fmt_pm "$elapsed" "$elapsed_sd")s"
            reg_disp=$(fmt_regression "$ppl" "$sweep_sd" "$baseline_ppl")
            printf "  %-10s %-10s %-6s %10s %10s %10s %14s %10s\n" \
                "$k_disp" "$v_disp" "-" "$ppl" "$sweep_sd" "$chunk_sd" "$reg_disp" "$time_disp"
        done
    fi

    echo "  --"
    echo "  sweep± = stdev across n_ctx values (different text offsets)"
    echo "  chunk± = mean within-file stdev (from llama-perplexity)"
