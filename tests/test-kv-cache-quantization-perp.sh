# --- Perplexity extraction ---

extract_ppl_and_sd() {
    local output="$1"
    local line
    line=$(echo "$output" | grep 'PPL = ' | tail -1)
    local ppl sd
    ppl=$(echo "$line" | sed -n 's/.*PPL = \([0-9.]*\).*/\1/p')
    sd=$(echo "$line" | sed -n 's/.*+\/- \([0-9.]*\).*/\1/p')
    sd="${sd:-0.0000}"
    echo "$ppl $sd"
}

